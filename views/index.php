<h2>SYSTEMY BARIER DROGOWYCH</h2>
<hr>
<p class="lead">Jesteśmy średniej wielkości firmą rodzinną, działającą w sektorze konstrukcji
    stalowych, z siedzibą zarządu mieszczącą się w Wilnsdorf, Niemcy. Mamy ponad 50 lat tradycji w
    produkcji i instalowaniu stalowych barier ochronnych na rynku niemieckim i na świecie. Jako członek
    – założyciel stowarzyszenia Guetegemainschaft e.V. w Siegen, Niemcy, produkujemy wg RAL-RG 620 (
    lub TL-SP ). Wszystkie produkowane systemy stalowych barier ochronnych, zakres dostawy i montażu
    jest zgodny z europejską normą EN 1317.</p>
